getFeatureMatrixArchR <- function(proj, embedding_name, ndim, corCutOff = 0.75) {
	suppressPackageStartupMessages({
	require(ArchR)})
	
	mobj <- getReducedDims(
    ArchRProj = proj,
    reducedDims = embedding_name,
    returnMatrix = TRUE,
    scaleDims = FALSE,  # already decided when the reduced dimension is created
    dimsToUse = 1:ndim,
    corCutOff = corCutOff
  )
	return(mobj)
}


runArchR_tiles <- function(fragfiles, output, genome, resolutions, ndim, tileSize=500, nfeatures=NULL){
	suppressPackageStartupMessages({
		require(ArchR)
		require(rhdf5)
		require(parallel)
		require(dplyr)
	})

	n_iteration <- length(resolutions)

	addArchRGenome(genome)
	addArchRVerbose(verbose = FALSE)

	fragfile_list <- strsplit(fragfiles, ",")
	# create arrow file
	ArrowFiles <- createArrowFiles(
	  inputFiles = unlist(fragfile_list) ,
	  sampleNames = paste0("CellinFile",seq(1,length(fragfile_list))),
	  minTSS = 0,
	  minFrags = 0,
	  addTileMat = FALSE,
	  addGeneScoreMat = FALSE,
	  force = FALSE
	)

	# create archR project
	proj <- ArchRProject(
	  ArrowFiles = ArrowFiles,
	  outputDirectory = paste0(output, "/proj"),
	  copyArrows = TRUE #This is recommended so that if you modify the Arrow files you have an original copy for later usage.
	)

	# add Tile matrix
	addTileMatrix(
		input = proj,
	    chromSizes = if (inherits(proj, "ArchRProject")) getChromSizes(proj) else NULL,
	    blacklist = if (inherits(proj, "ArchRProject")) getBlacklist(proj) else NULL,
	    tileSize = tileSize,
	    binarize = TRUE,
	    excludeChr = c("chrM", "chrY"),
	    threads = getArchRThreads(),
	    parallelParam = NULL,
	    force = TRUE,
	    logFile = createLogFile("addTileMatrix")
	)
    
    if(is.null(nfeatures)){nfeatures <- 25000}
    message(paste0("Number of features for iterative LSI in ArchR: ", nfeatures, "."))
	proj <- addIterativeLSI(
		ArchRProj = proj,
	    useMatrix = "TileMatrix",
	    name = paste0("IterativeLSI_ndim",ndim),
	    iterations = n_iteration,
	    clusterParams = list(
		resolution = resolutions, 
		sampleCells = NULL,
		n.start = 10,
		algorithm = 3
		),
        sampleCellsPre = NULL,
        sampleCellsFinal = NULL,
	    varFeatures = nfeatures,
	    dimsToUse = 1:ndim,
	    scaleDims = TRUE,
	    force = TRUE
	  )

	return(proj)
	}


runArchR_peaks <- function(fragfiles, output, genome, macs2_path, resolutions, ndim, tileSize=500, nfeatures=NULL){
	proj <- runArchR_tiles(fragfiles, output, genome, resolutions, ndim, tileSize, nfeatures)

	# Clustering
	proj <- addClusters(
    input = proj,
    reducedDims = paste0("IterativeLSI_ndim",ndim),
    method = "Seurat",
    name = paste0("Clusters_ndim",ndim),
    resolution = tail(resolutions,n=1),
    algorithm = 3,
    dimsToUse = 1:ndim,
    force = TRUE
    )

	# Adding pseudo-bulk
	proj <- addGroupCoverages(ArchRProj = proj, groupBy = paste0("Clusters_ndim",ndim), force = TRUE)

	# Calling peaks using MACS2
	proj <- addReproduciblePeakSet(
    ArchRProj = proj,
    groupBy = paste0("Clusters_ndim",ndim),
    peakMethod = "Macs2",
    pathToMacs2 = macs2_path
    )

	# Add insertion matrix
	proj <- addPeakMatrix(proj, binarize = FALSE)

	# Redo LSI
	n_iteration <- length(resolutions)
    
    if(is.null(nfeatures)){nfeatures <- 25000}
    message(paste0("Number of features for iterative LSI in ArchR: ", nfeatures, "."))
    
	proj <- addIterativeLSI(
	ArchRProj = proj,
	useMatrix = "PeakMatrix",
	name = paste0("IterativeLSI_peaks_ndim",ndim),
	iterations = n_iteration,
	clusterParams = list( #See Seurat::FindClusters
	resolution = resolutions,
	sampleCells = NULL,
	n.start = 10,
	algorithm = 3
	),
    sampleCellsPre = NULL,
    sampleCellsFinal = NULL,
	varFeatures = nfeatures,
	dimsToUse = 1:ndim,
	scaleDims = TRUE,
	force = TRUE)

	return(proj)
}